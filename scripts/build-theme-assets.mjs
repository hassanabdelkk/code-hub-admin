#!/usr/bin/env node
/**
 * Liest alle binären Assets in src/landing-themes/<theme>/assets/ ein und
 * schreibt sie als Base64-Map nach src/lib/theme-assets.generated.ts.
 * Diese Map wird im Landing-Generator verwendet, um Theme-Bilder in die
 * ausgelieferte ZIP zu packen (Cloudflare-Worker-kompatibel, keine FS-Zugriffe
 * zur Laufzeit).
 *
 * Manuell ausführen nach Änderungen an Theme-Assets:
 *   node scripts/build-theme-assets.mjs
 */
import { readdirSync, readFileSync, writeFileSync, statSync } from "node:fs";
import { join } from "node:path";

const THEMES_DIR = "src/landing-themes";
const OUT = "src/lib/theme-assets.generated.ts";

function listAssets(themeDir) {
  const assetsDir = join(THEMES_DIR, themeDir, "assets");
  try {
    return readdirSync(assetsDir).filter((f) => {
      const full = join(assetsDir, f);
      return statSync(full).isFile();
    });
  } catch {
    return [];
  }
}

const themes = readdirSync(THEMES_DIR).filter((d) => {
  try { return statSync(join(THEMES_DIR, d)).isDirectory(); } catch { return false; }
});

const map = {};
let count = 0, bytes = 0;
for (const theme of themes) {
  const files = listAssets(theme);
  if (!files.length) continue;
  map[theme] = {};
  for (const f of files) {
    const buf = readFileSync(join(THEMES_DIR, theme, "assets", f));
    map[theme][f] = buf.toString("base64");
    count++;
    bytes += buf.length;
  }
}

const header =
`// AUTO-GENERATED by scripts/build-theme-assets.mjs — do not edit by hand.
// Base64-codierte Theme-Assets, im Worker-Bundle eingebettet.
export const THEME_ASSETS: Record<string, Record<string, string>> = `;

writeFileSync(OUT, header + JSON.stringify(map, null, 2) + ";\n");
console.log(`✓ ${count} Asset(s), ${(bytes/1024).toFixed(1)} KB → ${OUT}`);
